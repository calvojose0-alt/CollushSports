export function getRaceById(raceId) {
  return RACES_2026.find((r) => r.id === raceId) || null
}

export function getCurrentRace() {
  const now = new Date()
  // Find the next race that hasn't been locked yet
  const upcoming = RACES_2026.filter((r) => new Date(r.lockTime) > now)
  return upcoming[0] || RACES_2026[RACES_2026.length - 1]
}

export function isRaceLocked(raceId) {
  const race = getRaceById(raceId)
  if (!race) return true
  return new Date() >= new Date(race.lockTime)
}
